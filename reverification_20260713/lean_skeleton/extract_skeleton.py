"""Extract the exact-arithmetic skeleton of the perceptron certificates into Lean.

Reads the canonical certificate JSONs of the public proof tree and emits
self-contained Lean 4 files whose kernel-checked theorems restate, over the
integers, the exact-rational claims that the Python verifiers check with
``fractions.Fraction``:

  1. Region I (huang_region1.json): for every band, the serialized Loewner
     majorant packets prove positive definiteness:
         b11_lo > 0,  b22_lo > 0,  b11_lo * b22_lo > max(|b12_lo|,|b12_hi|)^2,
     where packet endpoints are (mid -/+ rad) * 10^exp10.
  2. Region I localization: for every band, the localization leaves grouped by
     (edge, base_segment) form a complete, prefix-free, duplicate-free binary
     cover of depth <= 14  (the verifier's _complete_prefix_tree).
  3. Block 3b/3c replay lanes (b_neg, b_pos, c): every leaf's arb packet
     proves its claimed sign, leaves tile each job interval exactly, and jobs
     tile the lane's schedule boundaries exactly.

Every rational claim is integer-cleared (positive denominators throughout, so
sign and order are preserved).  The Lean side re-decides each claim with the
kernel's bignum arithmetic; no floating point, no Python, no Arb is trusted.

The same extracted data is re-checked here with Fraction arithmetic (the
"mirror") so that an extraction bug cannot silently weaken the Lean claims:
mirror failure aborts generation.

Outputs (under --out):
  Defs header embedded in every generated file (self-contained; no lake).
  R1_BDef_XX.lean, R1_Cover_XX.lean, B3bc_signs.lean, and manifest.json
  binding input and output SHA-256 hashes.

This is deliberately NOT a formalization of the analytic mathematics (Gaussian
integrals, entropy duality, ray concavity).  It kernel-checks the exact
combinatorial/algebraic layer of the certificate chain in a second, foreign
proof system.
"""

from __future__ import annotations

